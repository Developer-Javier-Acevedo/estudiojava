

public class Operacion {

    

          
    public int  SumaDeNumeros(int numero1, int numero2){

        int suma = numero1 + numero2; 
        return suma; 
    }

    public int  RestaDeNumeros(int numero1, int numero2){

        int resta = numero1 -  numero2; 
        return resta;
    }
    public int DivisionDeNumeros(int numero1, int numero2){

        int division = numero1 / numero2; 
        return division;

    }
    public int MultiplicacionDeNumeros(int numero1, int numero2){

        int multiplicacion = numero1 * numero2; 
        return multiplicacion;
    }


    

  



    
}
